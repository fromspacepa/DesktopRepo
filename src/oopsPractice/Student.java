package oopsPractice;

public class Student {
	
	
	String firstName;
	String lastName;
	int age;
	String phoneNo;
	String address;
	String subject;
	String email;
	
	public Student() {
		firstName = "";
		lastName = "";
		age = 0;
		phoneNo = "";
		address = "";
		subject = "";
		email = "";
	}
	
	public Student(String newFirstName, String newLastName, int newAge, String newPhoneNo, String newAddress, String newSubject, String newEmail) {
		firstName = newFirstName;
		lastName = newLastName;
		age = newAge;
		phoneNo = newPhoneNo;
		address = newAddress;
		subject = newSubject;
		email = newEmail;
	
		
	
				
		
	}
		

	

}
