package javaBasics;

public class DoWhileLoop {

	public static void main(String[] args) {

		
		//Example of While loop:
		int x = 10;
		while (x > 10 && x <= 20) {
			System.out.println(x++);
			}
		
		//Example of Do-while loop:
		int y = 5;
		do {
			System.out.println(y--);
		} while (y > 5 && y <= 10);
		
		
		
		
		
		
		
		

	}

}
